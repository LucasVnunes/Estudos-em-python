#faça um programa que leia a largura e a altura de uma parede em metros, calcule a sua área e a quantidade de tinta necessaria para pintá-la, sabendo que cada litro da tinta, pinta uma area de 2m*2

largura = int(input("digite a largura: "))
Altura = int(input("digite a largura: "))
area = largura * Altura
quantidade_necessaria = area / 2
print (quantidade_necessaria)

